package com.multi.member;

public class JoinMemberVO {
	private String joName;
	private String joID;
	private String joPassword;
	private String joEmail;
	private String joYear;
	private String joMonth;
	private String joDate;
	
	
	public String getJoName() {
		return joName;
	}
	public void setJoName(String joName) {
		this.joName = joName;
	}
	public String getJoID() {
		return joID;
	}
	public void setJoID(String joID) {
		this.joID = joID;
	}
	public String getJoPassword() {
		return joPassword;
	}
	public void setJoPassword(String joPassword) {
		this.joPassword = joPassword;
	}
	public String getJoEmail() {
		return joEmail;
	}
	public void setJoEmail(String joEmail) {
		this.joEmail = joEmail;
	}
	public String getJoYear() {
		return joYear;
	}
	public void setJoYear(String joYear) {
		this.joYear = joYear;
	}
	public String getJoMonth() {
		return joMonth;
	}
	public void setJoMonth(String joMonth) {
		this.joMonth = joMonth;
	}
	public String getJoDate() {
		return joDate;
	}
	public void setJoDate(String joDate) {
		this.joDate = joDate;
	}
	
	
	
}
